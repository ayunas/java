package com.company;

public class Switch {

    public static void main(String[] args) {
        char character = 'e';

        switch(character) {
            case 'a':
                System.out.println("character is an a");
                break;
            case 'b':
                System.out.println("B cool!!!");
                break;
            case 'c': case 'd': case 'e':
                System.out.println("sooo cooooool cccccaaatt");
                break;
            default:
                System.out.println("close but no cigar");
        }


        



    }
}


